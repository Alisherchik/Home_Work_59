#Упражнение 6: Налоги и чаевые
# Запрашиваем у пользователя сумму заказа
order_amount = float(input("Введите сумму заказа в ресторане: "))

# Определяем процент налога и чаевых
tax_rate = 0.10  # 10% налог
tip_rate = 0.18  # 18% чаевые

# Вычисляем сумму налога и чаевых
tax = order_amount * tax_rate
tip = order_amount * tip_rate

# Вычисляем общую сумму заказа с учетом налога и чаевых
total = order_amount + tax + tip

# Выводим результаты, округляя их до двух знаков после запятой
print(f"Налог: {tax} сум")
print(f"Чаевые: {tip} сум")
print(f"Общая сумма: {total} сум")