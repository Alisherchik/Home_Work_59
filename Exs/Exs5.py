# Упражнение 5: Сдаём бутылки
# Задаём стоимость бутылок разного объёма
small_bottle_price = 0.10  # Цена за бутылку объёмом Lesson 23 литр и меньше
large_bottle_price = 0.25  # Цена за бутылку объёмом больше Lesson 23 литра

# Запрашиваем у пользователя количество бутылок каждого размера
small_count = int(input("Введите количество бутылок Lesson 23 литр и меньше: "))
large_count = int(input("Введите количество бутылок больше Lesson 23 литра: "))

# Вычисляем общую сумму, которую можно выручить
total = small_count * small_bottle_price + large_count * large_bottle_price

# Выводим итоговую сумму, форматируя её до двух знаков после запятой
print(f"Вы можете выручить: {total} сум")